<H4 style="TEXT-ALIGN: center"><STRONG>WELCOME TO THE<BR>INVADING SPECIES<BR>AWARENESS PROGRAM WEBSITE</STRONG></H4> 
<P style="TEXT-ALIGN: justify">Invading species are one of the greatest threats to the biodiversity of Ontario’s waters and woodlands. Originating from other regions of the world, and in the absence of their natural predators or controls, invading species can have devastating effects on native species, habitats and ecosystems.</P> 
<P style="TEXT-ALIGN: justify">In 1992, the <A class=ext-link title="" href="http://www.ofah.org/" rel=nofollow target=_blank><FONT color=#0066cc>Ontario Federation of Anglers and Hunters</FONT></A>, in partnership with the <A class=ext-link title="" href="http://www.mnr.gov.on.ca/" rel=nofollow target=_blank><FONT color=#0066cc>Ontario Ministry of Natural Resources</FONT></A>, established the Invading Species Awareness Program in order to address the increasing threats posed by invading species in Ontario. Our objectives are to generate education and awareness of aquatic and terrestrial invading species, address key pathways contributing to invading species introductions and spread, and facilitate monitoring and tracking initiatives for the spread of new invaders found within Ontario.</P> 
