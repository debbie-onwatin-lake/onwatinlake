<H2 class=deck><DECK></DECK></H2> 
<P class=author>By: <AUTHOR>Sudbury Northern Life Staff</AUTHOR></P><ARTICLE> 
<P>By Lindsay Kelly</P> 
<P>For Northern Life</P> 
<P></P> 
<P>They’re small, they’re native to the area and, best of all, they have a huge appetite for Eurasian watermilfoil, an invasive species that’s threatening to choke the life out of Sudbury’s water bodies.</P> 
<P></P> 
<P>Milfoil weevils are being released as a biocontrol in five of Sudbury’s lakes—Long, Simon, Grant, Richard and McFarlane—in an effort to reduce the Eurasian watermilfoil population. An aquatic beetle the size of a sesame seed, the milfoil weevil larvae burrows into the stem of the plant, preventing it from growing and holding nutrients, while the adult insect eats the leaves.</P> 
<P></P> 
<P>This is the second year of a three-year project jointly administered by the City of Greater Sudbury and the Greater Sudbury Watershed Alliance, with assistance from EnvrioScience, an Ohio-headquartered firm that is now moving into Ontario.</P> 
<P></P> 
<P>Bob Florean, Ontario outreach specialist with Milfoil Solution, the Ontario arm of EnviroScience, said invasive species such as Eurasian watermilfoil are negatively impacting the Canadian economy to the tune of billions of dollars a year.</P> 
<P></P> 
<P>Eurasian milfoil grows rapidly, choking out native plants and interfering with recreational activity on the water. Eradication alternatives used in other jurisdictions include herbicides and chemical treatments, Florean said. But more people are looking for a natural solution, and even cutting out the plants doesn’t always work since they can regrow from even a tiny slip.</P> 
<P></P> 
<P>“(Weevils are) probably most effective because it works in concert with the ecosystem, and it’s environmentally friendly,” Florean said.</P> 
<P></P> 
<P>Lara Roketenetz, a PhD candidate in integrated biosciences with the University of Akron, said that the more weevils there are in an area, the greater effect they will have on the Eurasian watermilfoil population.</P> 
<P></P> 
<P>“Because it’s augmented biocontrol, the point of the project is to not mimic a natural population of weevils,” she said, “but to put a large number of weevils in the same age class in a small area so that when they hatch out, through their pupation stage, they can easily find each other and then mate again, and that’s where the bang of the project happens.”</P> 
<P></P> 
<P>Funding was provided by the city, and a lab—the first of its kind in Ontario—has been set up at Collège Boréal where 11 of its students have been working to cultivate beetle populations and stock them in local lakes. More than 75,000 weevils were stocked last year, and another 44,000 will be stocked by the end of this season.</P> 
<P></P> 
<P>Stephen Monet, Sudbury’s manager of environmental initiatives, said Sudburians will benefit from the project, since property values have been shown to decrease by nearly 20 per cent when watermilfoil is present. By controlling the invasion of Eurasian watermilfoil, people will benefit from increased property values, clearer water and the recreational use of local lakes.</P> 
<P></P> 
<P>In a news release, Mayor Marianne Matichuk commended city council’s foresight in approving the project last year.</P> 
<P></P> 
<P>“The City of Greater Sudbury is a leader in fighting and trying to preserve its lakes from invasive aquatic plants using a natural method,” she said. “Thanks to city council’s support of the project, we have been proactive as a community in seeking out solutions to manage local and problematic Eurasian milfoil issues.”</P> 
<P></P> 
<P><EM>- Posted by Jenny Jelen</EM></P></ARTICLE></DIV></DIV> 
