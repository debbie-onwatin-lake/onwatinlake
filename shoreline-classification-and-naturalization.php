<p><a href="http://www.greatersudbury.ca/cms/index.cfm?app=div_lakewaterquality&amp;currID=9976&amp;lang=en"><font color="#0066cc">What colour is your shoreline?</font></a></p> 
<h2>General Information</h2> 
<p><strong>Population<br></strong>Onwatin Lake has 159 permanent residents and 13 seasonal/recreational residents. </p> 
<p><strong>Location &amp; Lake Characteristics<br></strong>Onwatin Lake is located in Hanmer and Capreol townships, near the town of Hanmer. Onwatin Lake&nbsp;has an area of&nbsp;34.2 hectares and a shoreline perimetre of 5.4 kilometres. The maximum depth is 4 metres. Onwatin Lake is actually a widening of the Vermilion River. <a href="http://www.greatersudbury.ca/cms/index.cfm?app=div_lakewaterquality&amp;lang=en&amp;currID=4566&amp;parID=660"><font color="#0066cc">View&nbsp;maps of Onwatin Lake</font></a>.&nbsp;</p> 
<p><strong>Watershed Information<br></strong>Onwatin is located in the Upper Vermilion Watershed. For more information on the watershed, please click on the following link: <a href="http://www.city.greatersudbury.on.ca/lakewaterquality/maps/lakeindex.cfm" target="blank"><font color="#0066cc">Watershed Map</font></a>.</p> 
<p><strong>Lake Stewardship<br></strong>For information regarding the Onwatin Lake Stewardship&nbsp;Committee please click on the <a href="http://www.city.greatersudbury.on.ca/cms/index.cfm?app=div_lakewaterquality&amp;lang=en&amp;currID=696&amp;parID=0"><font color="#0066cc">Lake Stewardship link</font></a>.</p> 
<h2>Biological Characteristics</h2> 
<p><strong>Fish Species<br></strong>For more information on the different fish species found in Greater Sudbury lakes follow the <a href="http://www.city.greatersudbury.on.ca/cms/index.cfm?app=div_lakewaterquality&amp;currID=2177&amp;parID=0&amp;lang=en"><font color="#0066cc">fish link</font></a>.</p> 
<p><strong>Aquatic Insects<br></strong>For more information about macro invertebrates (aquatic insects), please visit the <a href="http://www.city.greatersudbury.on.ca/cms/index.cfm?app=div_lakewaterquality&amp;lang=en&amp;currID=683&amp;parID=0"><font color="#0066cc">Aquatic Insects Information</font></a> section of our website.</p> 
<p><strong>Aquatic Plants<br></strong>There is currently no data for aquatic plants.</p> 
