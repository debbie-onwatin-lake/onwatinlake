<P>In 2009 the Lake Water Quality Program started a new project on shoreline classification. Shorelines were measured using the chart above and classified with a colour indicating the amount of disturbance the shoreline has undergone. </P> 
<P><SPAN style="COLOR: #008000"><STRONG>Green</STRONG></SPAN> is the ideal type of shoreline, having a buffer zone will assist in filtering fertilizers, pesticides and waste that wash off your lawn/driveway and into the lakes. </P> 
<P>Planting shrubs and vegetation along the shoreline helps to stabilize the soil which will prevent erosion and requires less maintenance than a hard shoreline made of cement or rock that demands annual upkeep.<STRONG>&nbsp; </STRONG></P></DIV></div>
    
