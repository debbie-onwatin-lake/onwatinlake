<h2>Quality Characteristics</h2> 
<p><strong>Water Quality<br></strong>For lake water quality data of Onwatin Lake, follow the link to <a href="http://www.city.greatersudbury.on.ca/lakewaterquality/maps/lakedetail.cfm?lid=501123021"><font color="#0066cc">Onwatin Lake Data</font></a>.</p> 
<p><strong>Spring Phosphorus Results<br></strong>The 2010 spring phosphorus value for Onwatin Lake is&nbsp;8.0 µg/L, which is well below the Provincial Water Quality Objective of 20.0 µg/L and indicates that this lake is oligotrophic. </p> 
<p>For more detailed information, download the graph illustrating the spring phosphorus levels in Onwatin Lake from 1975-2010 <a href="http://www.greatersudbury.ca/content/div_lakewaterquality/documents/2010_OnwatinSPGraph.pdf" target="blank"><font color="#0066cc">(PDF&nbsp;9.22 KB)</font></a>.&nbsp;</p> 
