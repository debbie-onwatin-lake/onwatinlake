<H2 class=pageTitle>Lawn Fertilizer By-law</H2> 
<H3 class=MsoNoSpacing>Looking for lawn fertilizer? Zero in on the middle!</H3> 
<P>Effective April 1, a <A href="http://www.greatersudbury.ca/content/div_clerks/documents/By-law%202012-581.pdf" target=_blank><FONT color=#0066cc>new municipal by-law</FONT></A> (PDF 352.31 KB) restricts the use of lawn fertilizers containing phosphorus within the City of Greater Sudbury. You may no longer apply general-use lawn fertilizers containing phosphorus. <A href="http://www.greatersudbury.ca/content/div_lakewaterquality/documents/Zero%20In%20on%20the%20Middle%20-%20Fertilizer%20Campaign%20PDF%20Flyer%20-%20Mar%2028(ENG)1.pdf"><FONT color=#0066cc>Check to make sure the middle number of the ingredient formula is '0”.</FONT></A></P> 
<H4 style="MARGIN-RIGHT: 0px" dir=ltr>Phosphorus is still permitted in the following situations:</H4> 
<UL dir=ltr> 
<LI>When starting a new lawn from sod or seed during the first growing season</LI> 
<LI>When a test performed by an accredited soil testing service shows the soil's phosphorus level is not sufficient to support a lawn</LI> 
<LI>For agricultural application</LI> 
<LI>On sod farms and golf courses</LI></UL> 
<P>This by-law only relates to lawn fertilizers and does not affect other types of fertilizers, such as those used for flowers, vegetables, trees or shrubs. It does not restrict the sale of lawn fertilizers containing phosphorus – only their use.</P> 
<P>When applying any fertilizer, residents are reminded to avoid fertilizing when the ground is frozen, when it is raining or forecast to rain within 48 hours, or within 15 meters of any body of water. If fertilizer spills onto an impervious surface such as a walkway, it must be swept onto the soil to ensure absorption.</P> 
<P>If you're concerned about the health of your lawn, check the pH of your soil to make sure it isn't too acidic. You can purchase a simple soil test kit from local garden centres. If necessary, adding crushed limestone can decrease the acidity of the soil, which makes the nutrients more readily available to your grass.</P> 
<P>This by-law is intended to help protect the health of Greater Sudbury's lakes. In a community full of lakes, all chemicals, additives and products we use in and around our homes eventually reach the water. While phosphorus is a natural element needed for plant growth, too much phosphorus in a water body can be detrimental to the aquatic environment.</P> 
